# Pre-compile a single pattern for efficiency when filtering large batches.
# The pattern matches any keyword surrounded by word boundaries, case-insensitive.
_RELEVANCE_PATTERN: re.Pattern[str] = re.compile(
    "|".join(re.escape(kw) for kw in sorted(_RELEVANCE_KEYWORDS, key=len, reverse=True)),
    re.IGNORECASE,
)


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------


def is_relevant(text: str | None) -> bool:
    """
    Return ``True`` if *text* contains at least one relevance keyword.

    Matching is case-insensitive substring matching using a pre-compiled
    regex, so it handles multi-word keywords (e.g. ``"air strike"``) correctly.

    Parameters
    ----------
    text:
        Raw message text.  ``None`` / empty strings are treated as
        not relevant.
    """
    if not text:
        return False
    return bool(_RELEVANCE_PATTERN.search(text))


def filter_relevant(texts: list[str | None]) -> list[bool]:
    """
    Vectorised version of ``is_relevant`` for filtering a batch of messages.

    Parameters
    ----------
    texts:
        List of raw message texts.

    Returns
    -------
    list[bool]
        A list of the same length where each element indicates whether the
        corresponding text is relevant.
    """
    return [is_relevant(t) for t in texts]


def get_keywords() -> list[str]:
    """Return the current relevance keyword list (sorted for display)."""
    return sorted(_RELEVANCE_KEYWORDS)
